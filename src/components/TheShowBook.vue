<script setup>
import { onMounted, onUnmounted } from 'vue';
import { gsap } from 'gsap';
import ScrollTrigger from 'gsap/ScrollTrigger';



onMounted(() => {
    gsap.registerPlugin(ScrollTrigger)

    const sections = document.querySelectorAll('.section_show');
    sections.forEach(section => {
        
        const imgContent = section.querySelector('.img_content')
         const imgStop = section.querySelector('.img_stop')
       
            
            gsap.fromTo(imgContent, {
                // y:120,
            },
                {
                    yPercent: 0,
        
                    scrollTrigger: {
                        start: 'center center',
                        trigger: imgContent,
                        pin: true,
                        scrub: true,
                        id: 'sd',
                        // markers: true,
        
                        endTrigger:imgStop,
                        // endTrigger:
                        end: 'center bottom'
                        // end:() => "+=" + document.querySelector(".section").offsetWidth
                    }
                })
    
    })



    ScrollTrigger.getAll().forEach((element) => {
      element.enable();
      element.refresh();
      // console.log(element.vars);
    })

    // setTimeout(() => {
    //   ScrollTrigger.getAll().forEach((element) => {
    //     element.enable();
    //     element.refresh();
        
    //   })
    // }, 10)
})


onUnmounted(() => {
  ScrollTrigger.getAll().forEach((element) => {
      element.kill();
      // console.log({ element });
    });
})
</script>


 